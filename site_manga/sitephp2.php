        <section id="definition">
            <h2>Définition</h2>
            <p>
                Les animés sont des séries ou des films d'animation d'origine japonaise. Ils se distinguent par leur style artistique unique, leurs histoires captivantes et des personnages souvent très développés.
            </p>
        </section>

        <section id="histoire">
            <h2>Histoire</h2>
            <p>
                L'histoire des animés remonte au début du 20e siècle avec des courts-métrages d'animation. Depuis, le monde des animés a connu une croissance exponentielle et est devenu une partie intégrante de la culture populaire.
            </p>
        </section>

        <section id="genres">
            <h2>Genres</h2>
            <p>
                Les animés se déclinent en plusieurs genres, parmi lesquels :
            </p>
            <ul>
                <li>Isekai : souvent destiné aux garçons, avec un personnage réincarné.</li>
                <li>Shonen : souvent destiné aux garçons, souvent centré sur l'action.</li>
                <li>Shoujo : souvent destiné aux filles, axé sur la romance et les relations.</li>
                <li>Seinen : pour un public adulte, abordant des thèmes plus matures.</li>
                <li>Josei : ciblant les femmes, avec des histoires réalistes.</li>
                <li>Mecha : centré sur des robots géants et des batailles futuristes.</li>
            </ul>
        </section>

        <section id="conclusion">
            <h2>Conclusion</h2>
            <p>
                Les animés offrent une multitude d'histoires, de styles et d'émotions qui touchent des publics de tous âges. Que ce soit à travers des leçons de vie, des aventures épiques ou des romances émouvantes, les animés continuent de captiver les cœurs du monde entier.
            </p>
